"""Feasibility probe — codegen from a BUSINESS RULE + round-trip UNDERSTANDING (docs/codegen_understand.md).

This probe attacks the two open edges `spike_findings.md` names at the end of the spec->code
axis: (a) deriving the skeleton pool **compositionally** — slot-filling *beyond* whole-function
templates — and (b) **recognizing** generated code, to bridge business terminology with code. It
is a **probe, not integrated intake/analysis** (mirrors `experiments/spec_synthesis.py`).

Two findings, one line each:

  1. SYNTHESIS goes compositional.  `spec_synthesis.py` chose among whole-function TEMPLATES. Here a
     terse business rule ("compute accrual") is expanded by **recursive subgoal expansion**: the top
     need decomposes into sub-needs, each satisfied by a recipe that names a variable and builds its
     value from its own sub-needs, down to the leaf parameters. Code is written **bottom-to-top**
     (leaves first, composed upward, return at the top) — the doc's "recursive subgoal expansion".

  2. UNDERSTANDING is synthesis read backwards, and it is the SAME firmware.  We recognize code the
     system itself generated (match a topology -> *attribute* the function node "computes accrual"),
     and when a function was NOT generated by us (an arbitrary `sort`), recognition derives nothing,
     so we **supply the fact in CNL** (`mystery is_a sort_function`) — a directly-authored fact,
     with no derivation. That is the doc's "recognize round-trip; else supply info via CNL".

    analysis / spec_synthesis (built)         this probe
    ----------------------------------------  ------------------------------------------
    whole-function TEMPLATE selection         RECURSIVE subgoal expansion (compositional)
    `who realizes <spec>` (flat)              `who realizes <plan>` over a completeness DAG
    verify by re-execution                    verify by re-execution (SAME move: run it)
    (no reverse direction)                    RECOGNIZE generated code <-> business term

The two invariants of the whole project reappear and hold, which is the reassuring part:

  * RULES NEVER MINT.  A subgoal tree is existential-minting territory (fresh statements, fresh
    variables) — the wall `state_threading.py` and `spec_synthesis.py` both hit. So the emit tool
    pre-mints a bounded pool of RECIPES grouped into candidate PLANS (decompositions); the CNL rules
    only *select* a complete plan and CHOOSE the graded-best. The pool size is the synthesis fuel.

  * TRUST BY EXECUTION, never by claim.  Both emitted decompositions are RUN on sample inputs and
    checked to compute `principal * rate * days / 365` — the generator is believed only because the
    code re-executes correct (the design's concrete-exec tool in miniature; safe on pure skeletons).

**The non-trivial finding — a readability flip, the mirror of spec_synthesis's strictness flip.**
Two decompositions realize "compute accrual": an INLINE one (`accrual = principal*rate*days/365`,
one statement, compact) and a STEPWISE one (named intermediates `annual_interest`, `day_fraction`,
three statements, readable). Both compute the same number — CHOOSE picks the compact inline by
default. But add one word to the spec (`readable`) and the winner FLIPS: `readable` requires the
feature `named_steps`, which only the stepwise decomposition provides, so the inline plan stops
realizing the spec at all. Same shape as `return v or {}` vs the explicit ifexp: an added
requirement excludes the compact form because compactness no longer buys what the spec now demands.
"""
from __future__ import annotations

import ast
from dataclasses import dataclass, field
from typing import Callable

import ugm as h
from ugm import load_machine_rules, ask_goal, set_candidate, choose, explain_choice


# --- the succinct business spec (DATA) -------------------------------------------------------

@dataclass(frozen=True)
class Spec:
    """A terse business specification. `intent` names an entry in the recipe library; `readable`
    is the one flag whose expansion flips CHOOSE's winner. Deliberately SHORT — the CNL rules
    expand it into the concrete requirements the emitted code must satisfy."""
    name: str                    # graph id, e.g. "accrual_spec"
    intent: str                  # "accrual"
    fn_name: str                 # emitted function name
    readable: bool = False       # also require named intermediate steps (excludes the inline plan)


# the one feature a spec can demand of the DECOMPOSITION (not of a single statement) — this is the
# compositional analogue of spec_synthesis's per-statement features.
FEATURES = ("named_steps",)


# --- the emit tool: a pre-minted pool of RECIPES grouped into candidate PLANS ------------------
# Rules cannot MINT fresh code (the existential wall). So the tool pre-mints the recipe pool; the
# rules only SELECT a complete plan. A recipe satisfies one NEED by BINDING a variable to a value
# built from its own sub-needs — the subgoal node of a recursive expansion. A leaf need is a
# function PARAMETER (bound at the boundary, built by nothing).

def _name(v: str) -> ast.Name:
    return ast.Name(id=v, ctx=ast.Load())


def _mul(*xs: ast.expr) -> ast.expr:
    acc = xs[0]
    for x in xs[1:]:
        acc = ast.BinOp(left=acc, op=ast.Mult(), right=x)
    return acc


def _div(a: ast.expr, b: ast.expr) -> ast.expr:
    return ast.BinOp(left=a, op=ast.Div(), right=b)


@dataclass(frozen=True)
class Recipe:
    name: str                            # graph id / library key
    produces: str                        # the NEED it satisfies (== the variable it binds)
    needs: tuple[str, ...]               # the sub-needs it depends on (resolved recursively)
    build: Callable[[dict[str, ast.expr]], ast.expr]  # sub-need -> value-expr => this need's value


# the leaf parameters — the boundary needs, satisfied by nothing (the base case of the expansion).
PARAMS = ("principal", "rate", "days")

# the recipe pool. `days_of_year` is a constant leaf so the arithmetic reads from named needs only.
RECIPES: dict[str, Recipe] = {r.name: r for r in [
    # --- the STEPWISE decomposition: accrual <- annual_interest x day_fraction, each named ---
    Recipe("r_annual_interest", "annual_interest", ("principal", "rate"),
           lambda v: _mul(v["principal"], v["rate"])),
    Recipe("r_day_fraction", "day_fraction", ("days",),
           lambda v: _div(v["days"], ast.Constant(value=365))),
    Recipe("r_accrual_stepwise", "accrual", ("annual_interest", "day_fraction"),
           lambda v: _mul(v["annual_interest"], v["day_fraction"])),
    # --- the INLINE decomposition: accrual straight from the leaf parameters, one statement ---
    Recipe("r_accrual_inline", "accrual", ("principal", "rate", "days"),
           lambda v: _div(_mul(v["principal"], v["rate"], v["days"]), ast.Constant(value=365))),
]}


@dataclass(frozen=True)
class Plan:
    """A candidate DECOMPOSITION: a set of recipes whose root produces `accrual`. `provides` are the
    features it guarantees (matched to the spec's derived `requires`); `compactness` grades it."""
    name: str                    # graph id / library key
    for_intent: str              # the intent this plan is a decomposition of
    recipes: tuple[str, ...]     # the recipe names it composes
    provides: frozenset[str]
    compactness: float           # higher = simpler/smaller edit = preferred (mirror of spec_synth)


PLANS: list[Plan] = [
    Plan("plan_inline", "accrual", ("r_accrual_inline",), frozenset(), 1.0),
    Plan("plan_stepwise", "accrual",
         ("r_annual_interest", "r_day_fraction", "r_accrual_stepwise"),
         frozenset({"named_steps"}), 0.7),
]
_PLAN_BY_NAME = {p.name: p for p in PLANS}


# --- the refinement rules (CNL) — recursive subgoal expansion + realization -------------------
# Stratified, pure Datalog (binds pre-minted spec/recipe/plan nodes, mints nothing):
#   COVERAGE (recursive):
#     C1 a leaf param is covered in any plan (the base case of the expansion).
#     C2 a need is covered in a plan if some recipe IN THAT PLAN produces it.
#     G1 a plan has a GAP if a recipe in it needs a sub-need nothing in the plan covers (one NAC).
#     K1 a plan is COMPLETE iff it is a plan with no gap (one NAC over the gap) — i.e. the whole
#        subgoal tree bottoms out at parameters. This is the recursive-expansion check, as Datalog.
#   REALIZATION (the mirror of spec_synthesis, now over PLANS):
#     D1 DECOMPOSE : the `readable` flag expands into the concrete required feature `named_steps`.
#     L1 LACKS     : a plan lacks a feature it does not provide (NAC over the feature vocab).
#     M1 MISSES    : a plan misses a spec if it lacks any feature that spec requires.
#     R1 REALIZE   : a plan realizes a spec iff it is COMPLETE, is for that intent, and misses
#                    nothing (completeness AND the conjunction-of-features, both as `not ...`).
#   RECOGNIZE (understanding = the SAME machinery, backwards):
#     U1 a function COMPUTES an intent when a complete plan for that intent was emitted as it.
RULES = "\n".join([
    # coverage / completeness (the recursive subgoal expansion, checked as stratified Datalog)
    "?need covered_in ?plan when ?need is_a param and ?plan is_a plan",
    "?need covered_in ?plan when ?r in_plan ?plan and ?r produces ?need",
    "?plan has_gap yes when ?r in_plan ?plan and ?r needs ?sub and not ?sub covered_in ?plan",
    "?plan complete yes when ?plan is_a plan and not ?plan has_gap yes",
    # realization over plans (the mirror of spec_synthesis's `realizes iff not misses`)
    "?spec requires named_steps when ?spec is_a spec and ?spec readable yes",
    "?plan lacks ?feat when ?feat is_a feature and ?plan is_a plan and not ?plan provides ?feat",
    "?plan misses ?spec when ?spec requires ?feat and ?plan lacks ?feat",
    "?plan realizes ?spec when ?plan is_a plan and ?plan complete yes and ?plan for_intent ?k "
    "and ?spec intent ?k and not ?plan misses ?spec",
    # recognition: understanding is synthesis backwards, over the SAME completeness derivation
    "?fn computes ?k when ?plan emitted_as ?fn and ?plan for_intent ?k and ?plan complete yes",
])


def _planning_graph(spec: Spec, emitted: dict[str, str] | None = None) -> "h.Graph":
    """Materialize the spec + recipe pool + candidate plans + leaf params + feature vocab as facts
    (the one direct-authoring boundary — the reverse of intake). `emitted` optionally links a plan
    to the function node it was emitted as (`plan emitted_as fn`), for round-trip recognition —
    threaded through the same node cache so it binds the SAME plan node the completeness rule sees."""
    g = h.Graph(); ids: dict[str, str] = {}
    def n(x: str) -> str:
        if x not in ids: ids[x] = g.add_node(x)
        return ids[x]
    def rel(s: str, p: str, o: str) -> None: g.add_relation(n(s), p, n(o))

    for feat in FEATURES:
        rel(feat, "is_a", "feature")
    for p in PARAMS:
        rel(p, "is_a", "param")
    rel(spec.name, "is_a", "spec")
    rel(spec.name, "intent", spec.intent)
    if spec.readable:
        rel(spec.name, "readable", "yes")
    for plan in PLANS:
        rel(plan.name, "is_a", "plan")
        rel(plan.name, "for_intent", plan.for_intent)
        for feat in plan.provides:
            rel(plan.name, "provides", feat)
        for rname in plan.recipes:
            r = RECIPES[rname]
            rel(rname, "in_plan", plan.name)
            rel(rname, "produces", r.produces)
            for need in r.needs:
                rel(rname, "needs", need)
    for plan_name, fn_name in (emitted or {}).items():
        rel(plan_name, "emitted_as", fn_name)
    return g


def retrieve(spec: Spec) -> list[Plan]:
    """Backward-CHAIN the rules: which COMPLETE plans realize this spec? Entirely through the public
    `ask_goal` — the mirror of `operators.retrieve` / `spec_synthesis.retrieve`."""
    rules = load_machine_rules(RULES)
    g = _planning_graph(spec)
    answers = ask_goal(g, f"who realizes {spec.name}", rules)
    names = {a.split(" ", 1)[0] for a in answers}
    return [_PLAN_BY_NAME[nm] for nm in names if nm in _PLAN_BY_NAME]


def is_complete(spec: Spec, plan: str) -> bool:
    """Does the recursive subgoal expansion bottom out at parameters for this plan? Proved through
    the public firmware (the compositional analogue of a spec's derived requirement)."""
    rules = load_machine_rules(RULES)
    return ask_goal(_planning_graph(spec), f"is {plan} complete yes", rules) == ["yes"]


def realize_trace(spec: Spec, plan: str) -> list[str]:
    """RECORD provenance for WHY a plan realizes the spec — the spec->code rationale (mirror of the
    execution trace), threading back through completeness and the derived requirements."""
    rules = load_machine_rules(RULES)
    return ask_goal(_planning_graph(spec), f"why {plan} realizes {spec.name}", rules)


# --- CHOOSE the graded-best decomposition (mirror of analysis._choose) ------------------------

def choose_plan(cands: list[Plan]) -> tuple[Plan | None, list[str]]:
    g = h.Graph()
    goal = g.add_node("codegen_goal")
    node_of: dict[str, Plan] = {}
    for c in cands:
        opt = g.add_node(c.name)
        node_of[c.name] = c
        set_candidate(g, goal, opt, c.compactness)
    winners = choose(g, goal, alpha=0.01)
    return (node_of[g.name(winners[0])] if winners else None), explain_choice(g, goal)


# --- the emit tool: a plan -> real Python, written BOTTOM-TO-TOP ------------------------------

def _topo(plan: Plan) -> list[Recipe]:
    """Order a plan's recipes leaves-first: a recipe emits only after every recipe producing one of
    its needs has emitted. This IS the doc's 'code is written bottom to top'."""
    produced = {RECIPES[r].produces: RECIPES[r] for r in plan.recipes}
    order: list[Recipe] = []
    done: set[str] = set(PARAMS)
    pending = list(plan.recipes)
    while pending:
        progressed = False
        for rname in list(pending):
            r = RECIPES[rname]
            if all(need in done for need in r.needs):     # all sub-needs already available
                order.append(r); done.add(r.produces); pending.remove(rname); progressed = True
        if not progressed:                                 # a gap the completeness rule would reject
            raise ValueError(f"plan {plan.name} has an unresolvable need")
    assert produced  # every recipe placed
    return order


def emit(spec: Spec, plan: Plan) -> str:
    """Assemble the decomposition into `def fn(params): <bind each subgoal> return accrual` and
    unparse to real Python — the emit boundary, in reverse of intake's `ast`."""
    body: list[ast.stmt] = []
    value_of: dict[str, ast.expr] = {p: _name(p) for p in PARAMS}   # a need's value-expression
    for r in _topo(plan):
        rhs = r.build({need: value_of[need] for need in r.needs})
        body.append(ast.Assign(targets=[ast.Name(id=r.produces, ctx=ast.Store())], value=rhs))
        value_of[r.produces] = _name(r.produces)                    # later recipes read it by name
    body.append(ast.Return(value=_name("accrual")))
    fn = ast.FunctionDef(
        name=spec.fn_name,
        args=ast.arguments(posonlyargs=[], args=[ast.arg(arg=p) for p in PARAMS],
                           vararg=None, kwonlyargs=[], kw_defaults=[], kwarg=None, defaults=[]),
        body=body, decorator_list=[])
    mod = ast.Module(body=[fn], type_ignores=[])
    ast.fix_missing_locations(mod)
    return ast.unparse(mod)


# --- verify by re-execution (the design's concrete-exec tool, in miniature) -------------------

# a couple of sample inputs; the emitted function must compute the accrual formula on each.
_SAMPLES = [(1000.0, 0.05, 30.0), (250.0, 0.12, 90.0), (0.0, 0.05, 30.0)]


def _expected(principal: float, rate: float, days: float) -> float:
    return principal * rate * days / 365


def verify_computes_accrual(source: str, fn_name: str) -> bool:
    """CONCRETE check: RUN the emitted function on sample inputs and require it to compute
    `principal*rate*days/365`. Trust by execution, never by the recipe's claim. Safe: our own
    pre-minted, pure, side-effect-free bodies."""
    ns: dict[str, object] = {}
    exec(compile(source, "<emitted>", "exec"), ns)
    fn = ns[fn_name]
    return all(abs(fn(p, r, d) - _expected(p, r, d)) < 1e-9 for (p, r, d) in _SAMPLES)


# --- the whole synthesis loop ----------------------------------------------------------------

@dataclass
class Synthesis:
    spec: Spec
    retrieved: list[str]                 # the COMPLETE plans that realize the spec
    winner: str | None
    source: str
    computes_ok: bool                    # CONCRETE: the emitted winner computes the accrual formula
    realize_trace: list[str]             # RECORD: why the winner realizes the spec
    choose_trace: list[str]              # CHOOSE: why it beat the alternatives
    candidates: list[Plan] = field(default_factory=list)


def synthesize(spec: Spec) -> Synthesis:
    """spec -> RETRIEVE complete realizing plans -> CHOOSE the graded-best -> EMIT bottom-to-top ->
    VERIFY by re-execution. The compositional mirror of the analyze/repair loop, one firmware."""
    cands = retrieve(spec)
    winner, choose_trace = choose_plan(cands)
    source = emit(spec, winner) if winner else ""
    computes_ok = bool(winner) and verify_computes_accrual(source, spec.fn_name)
    return Synthesis(
        spec=spec, retrieved=sorted(c.name for c in cands),
        winner=winner.name if winner else None, source=source, computes_ok=computes_ok,
        realize_trace=realize_trace(spec, winner.name) if winner else [],
        choose_trace=choose_trace, candidates=cands)


# --- Part 2: UNDERSTANDING — round-trip recognition, else a CNL-supplied fact -----------------

def recognize(spec: Spec, plan_name: str, fn_name: str) -> list[str]:
    """RECOGNIZE code the system itself generated: attach the winning plan's topology to the emitted
    function node and derive `?fn computes <intent>` — understanding is synthesis run backwards over
    the SAME completeness derivation. Returns the recognized business terms for `fn_name`."""
    rules = load_machine_rules(RULES)
    g = _planning_graph(spec, emitted={plan_name: fn_name})
    return ask_goal(g, f"who computes {spec.intent}", rules)


def supply_fact_graph() -> "h.Graph":
    """A function we did NOT generate: `mystery` has no plan fingerprint, so recognition derives
    nothing. Externally SUPPLY the fact in CNL — `mystery is_a sort_function` — a directly-authored
    fact with no derivation (the doc's 'provide a fact without relying on fact derivation')."""
    g = h.Graph()
    g.add_relation(g.add_node("mystery"), "is_a", g.add_node("sort_function"))
    return g


# --- live walkthrough ------------------------------------------------------------------------

def _show(spec: Spec) -> None:
    r = synthesize(spec)
    flag = "READABLE (named steps required)" if spec.readable else "lenient (compact allowed)"
    print(f"=== spec: compute {spec.intent} — {flag} ===")
    print(f"  refine -> complete realizing plans: {r.retrieved}")
    print(f"  choose -> winner: {r.winner}")
    for line in r.source.splitlines():
        print(f"           {line}")
    print(f"  verify -> computes principal*rate*days/365 on samples: {r.computes_ok}"
          f"  => {'SPEC HOLDS' if r.computes_ok and r.winner else 'SPEC VIOLATED'}\n")


def main() -> None:
    base = dict(name="accrual_spec", intent="accrual", fn_name="compute_accrual")

    print("PART 1 — codegen from a business rule via recursive subgoal expansion\n")
    _show(Spec(**base))                          # lenient: compact inline plan wins
    _show(Spec(**base, readable=True))           # readable: the FLIP — only the stepwise plan realizes

    print("Why the flip: both decompositions compute the SAME number, so CHOOSE prefers the compact\n"
          "inline form — until the spec adds `readable`, which requires `named_steps`; only the\n"
          "stepwise plan provides it, so the inline plan stops realizing the spec at all.\n")

    print("RECORD -> spec->code rationale for the readable winner (note completeness + the feature):")
    for line in realize_trace(Spec(**base, readable=True), "plan_stepwise"):
        print(f"    {line}")

    print("\nPART 2 — understanding: recognize our OWN generated code, else supply a CNL fact\n")
    strict = Spec(**base, readable=True)
    recognized = recognize(strict, "plan_stepwise", strict.fn_name)
    print(f"  recognize (round-trip) -> `who computes accrual`: {recognized}")
    print("    (the system recognizes code IT generated — the business term is bridged to the code)\n")

    mystery = ask_goal(supply_fact_graph(), "who is_a sort_function", load_machine_rules(RULES))
    print(f"  arbitrary code has NO fingerprint -> `who computes accrual` derives nothing for it;")
    print(f"  so we SUPPLY the fact in CNL -> `who is_a sort_function`: {mystery}")
    print("    (a directly-authored fact, no derivation — the escape hatch for un-normalized code)")


if __name__ == "__main__":
    main()
